class Refrigerator {
    public static void main(String[] args) {
        String brand; // declaration
        System.out.println("I am printing declaration part of brand:");
        brand = "LG"; // initialization
        System.out.println("I am printing initialization part of brand: " + brand);
        int capacity; // declaration
        System.out.println("I am printing declaration part of capacity:");
        capacity = 260; // initialization (in liters)
        System.out.println("I am printing initialization part of capacity: " + capacity);
        double price; // declaration
        System.out.println("I am printing declaration part of price:");
        price = 18500.75; // initialization
        System.out.println("I am printing initialization part of price: " + price);
        boolean hasFreezer; // declaration
        System.out.println("I am printing declaration part of hasFreezer:");
        hasFreezer = true; // initialization
        System.out.println("I am printing initialization part of hasFreezer: " + hasFreezer);
        int doors; // declaration
        System.out.println("I am printing declaration part of doors:");
        doors = 2; // initialization
        System.out.println("I am printing initialization part of doors: " + doors);
        long serialNumber; // declaration
        System.out.println("I am printing declaration part of serialNumber:");
        serialNumber = 12345678901L; // initialization
        System.out.println("I am printing initialization part of serialNumber: " + serialNumber);
    }
}