class Shirt {
    public static void main(String[] args) {
        String brand;  // declaration
        System.out.println("I am printing declaration part of brand:");
        brand = "PeterEngland";  // initialization
        System.out.println("I am printing initialization part of brand: " + brand);
        double price;  // declaration
        System.out.println("I am printing declaration part of price:");
        price = 1499.99;  // initialization
        System.out.println("I am printing initialization part of price: " + price);
        long serialNumber;  // declaration
        System.out.println("I am printing declaration part of serialNumber:");
        serialNumber = 78945612301L;  // initialization
        System.out.println("I am printing initialization part of serialNumber: " + serialNumber);
        boolean isAvailable;  // declaration
        System.out.println("I am printing declaration part of isAvailable:");
        isAvailable = true;  // initialization
        System.out.println("I am printing initialization part of isAvailable: " + isAvailable);
        char size;  // declaration
        System.out.println("I am printing declaration part of size:");
        size = 'L';  // L = Large
        System.out.println("I am printing initialization part of size: " + size);
        int shirtId;  // declaration
        System.out.println("I am printing declaration part of shirtId:");
        shirtId = 701;  // initialization
        System.out.println("I am printing initialization part of shirtId: " + shirtId);
    }
}