class SmartWatch {

    public static void main(String[] args) {

        // 1. Declaration only
        String brand;

        // 2. Declaration with initialization
        int price = 3500;

        // 3. Boolean variable
        boolean bluetooth = true;

        // 4. Character variable
        char modelGrade = 'A';

        // 5. Double variable
        double batteryCapacity = 450.75;

        // 6. Long variable
        long serialNumber = 946567643210L;

        // 7. Float variable
        float screenSize = 1.75f;

        // 8. Byte variable
        byte warrantyYears = 2;

        // Initialization of declared variable
        brand = "Noise";

        System.out.println("Brand: " + brand);
        System.out.println("Price: " + price);
        System.out.println("Bluetooth Available: " + bluetooth);
        System.out.println("Model Grade: " + modelGrade);
        System.out.println("Battery Capacity: " + batteryCapacity);
        System.out.println("Serial Number: " + serialNumber);
        System.out.println("Screen Size: " + screenSize);
        System.out.println("Warranty Years: " + warrantyYears);

    }
}