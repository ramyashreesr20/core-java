class Shirts {
    public static void main(String[] args) {
        long orderNumber;  // declaration
        System.out.println("I am printing declaration part of orderNumber:");
        orderNumber = 2026001L;  // initialization
        System.out.println("I am printing initialization part of orderNumber: " + orderNumber);
        int shirtSize;  // declaration
        System.out.println("I am printing declaration part of shirtSize:");
        shirtSize = 40;  // initialization
        System.out.println("I am printing initialization part of shirtSize: " + shirtSize);
        char section;  // declaration
        System.out.println("I am printing declaration part of section:");
        section = 'A';  // initialization
        System.out.println("I am printing initialization part of section: " + section);
        boolean hasLogo;  // declaration
        System.out.println("I am printing declaration part of hasLogo:");
        hasLogo = true;  // initialization
        System.out.println("I am printing initialization part of hasLogo: " + hasLogo);
        String shirtColor;  // declaration
        System.out.println("I am printing declaration part of shirtColor:");
        shirtColor = "Blue";  // initialization
        System.out.println("I am printing initialization part of shirtColor: " + shirtColor);
        double price;  // declaration
        System.out.println("I am printing declaration part of price:");
        price = 499.99;  // initialization
        System.out.println("I am printing initialization part of price: " + price);
    }
}