class Ac {
    public static void main(String[] args) {
        long serialNumber;  // declaration
        System.out.println("I am printing declaration part of serialNumber:");
        serialNumber = 63738353683610L;  // initialization
        System.out.println("I am printing initialization part of serialNumber: " + serialNumber);
        int acId;  // declaration
        System.out.println("I am printing declaration part of acId:");
        acId = 101;  // initialization
        System.out.println("I am printing initialization part of acId: " + acId);
        char energyRating;  // declaration
        System.out.println("I am printing declaration part of energyRating:");
        energyRating = 'A';  // initialization
        System.out.println("I am printing initialization part of energyRating: " + energyRating);
        boolean isInverter;  // declaration
        System.out.println("I am printing declaration part of isInverter:");
        isInverter = true;  // initialization
        System.out.println("I am printing initialization part of isInverter: " + isInverter);
        String brand;  // declaration
        System.out.println("I am printing declaration part of brand:");
        brand = "Daikin";  // initialization
        System.out.println("I am printing initialization part of brand: " + brand);
        double price;  // declaration
        System.out.println("I am printing declaration part of price:");
        price = 45999.99;  // initialization
        System.out.println("I am printing initialization part of price: " + price);
    }
}